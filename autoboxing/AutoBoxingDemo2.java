package autoboxing;

public class AutoBoxingDemo2 {
    public static void main(String[] args) {
        Byte byteValue = (byte) 0;
        Short shortValue = (short) 1;
        Integer integerValue = 3;
        Long longValue = 4L;
        Float floatValue = 5.0f;
        Double doubleValue = 6.0d;
        Character characterValue = '6';
        Boolean booleanValue = true;
        byte b = byteValue;
        short s = shortValue;
        int i = integerValue;
        long l = longValue;
        float f = floatValue;
        double d = doubleValue;
        char c = characterValue;
        boolean b1 = booleanValue;
    }
}
